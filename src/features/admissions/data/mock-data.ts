import type { AdmissionLead, FollowUp, Activity, Note } from '../types'

const now = new Date()
const daysAgo = (d: number) => new Date(now.getTime() - d * 86400000).toISOString()
const daysFromNow = (d: number) => new Date(now.getTime() + d * 86400000).toISOString()

function activities(lead: string, items: Omit<Activity, 'id'>[]): Activity[] {
  return items.map((a, i) => ({ ...a, id: `${lead}-act-${i}` }))
}

function notes(lead: string, items: Omit<Note, 'id'>[]): Note[] {
  return items.map((n, i) => ({ ...n, id: `${lead}-note-${i}` }))
}

